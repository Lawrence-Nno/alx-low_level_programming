/**
 * mul - This function multiplies two integers
 * @a: is one of the integers to be multiplied
 * @b: is the other integer to be multiplied with
 * Return: Returns the result of the multiplication
 */

int mul(int a, int b)
{
	int result;

	result = (a * b);
	return (result);
}
