#include "main.h"
#include <stdio.h>

/**
 * print_to_98 - this function prints all natural numbers from n to 98
 *
 * @n: The number where the printing will start from
 *
 * Return: nothing to return for now
 */

void print_to_98(int n)
{
	if (n < 98)
	{
		while (n <= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n++;
		}
	}
	else if (n > 98)
	{
		while (n >= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n--;
		}
	}
	else
	{
		printf("98");
	}
	printf("\n");
}
